export

let letras: Array<string> = ["Paulo","Livia","Lucas"];

function filtrar(filtrado: Array<number>): string{
    for(let i = 0; i<letras.length; i++){
        if(letras[i].length >= 5){
            filtrado.push()
        }
    }
    return `${filtrado}`
}
